var retweetCount1 = document.querySelector(".retweets1")
console.log(retweetCount1.innerHTML);

retweetbutton1=document.querySelector(".retweet1");

retweetbutton1.addEventListener("click", function(){
    var retweets1=Number(retweetCount1.innerHTML);
    retweets1+=1;
    retweetCount1.innerHTML=retweets1;
    console.log(retweets1);
})

